package Homework01;

public class Task05 {
    public static void main(String[] args) {
        //The numbers from 1 to 100 in ascending order
        System.out.println("The numbers from 1 to 100 in ascending order:");
        for (int i=1; i<=100; i++) {
            System.out.println(i);
        }

        //The numbers from 1 to 100 in descending order
        System.out.println("\nThe numbers from 1 to 100 in descending order:");
        for (int j=100; j>=1; j--) {
            System.out.println(j);
        }
    }
}
